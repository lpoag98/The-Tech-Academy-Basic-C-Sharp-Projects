using System;
using System.Collections.Generic;
using System.Text;

namespace Method_Assignment
{
    class MathOperation
    {
        // Method to do math operation with two integers
        public int DoMath(int num1, int num2)
        {
            // Example math operation: return sum of two numbers
            return num1 + num2;
        }

        // Method to do math operation with one integer
        public int DoMath(int num1)
        {
            // Example math operation: return square of the number
            return num1 * num1;
        }
    }
}
